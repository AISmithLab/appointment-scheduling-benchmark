from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path
from typing import Any

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT))

from grading.grader import solve_task


DEFAULT_TASKS_PATH = ROOT / "tasks" / "tasks.json"


def load_json(path: Path) -> Any:
    with path.open("r", encoding="utf-8") as file:
        return json.load(file)


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--tasks-file",
        default=str(DEFAULT_TASKS_PATH),
    )
    parser.add_argument("--output", default=None)
    args = parser.parse_args()

    tasks_path = Path(args.tasks_file)
    if args.output:
        output_path = Path(args.output)
    elif tasks_path.name == "tasks_hard.json":
        output_path = ROOT / "tasks" / "GROUND_TRUTH_HARD.md"
    else:
        output_path = ROOT / "tasks" / "GROUND_TRUTH.md"

    tasks = load_json(tasks_path)["tasks"]
    lines = [
        "# Appointment Benchmark Ground Truth",
        "",
        "Generated by the deterministic solver. Do not include this file in an agent prompt.",
        "",
    ]

    for task in tasks:
        oracle = solve_task(task)
        lines.extend(
            [
                f"## {task['id']}: {task['name']}",
                "",
                f"- **Suite:** {task.get('suite', '')}",
                f"- **Skill:** {task.get('skill', '')}",
                f"- **Expected action:** {oracle['action']}",
                f"- **Expected slot:** {oracle['new_slot_id'] or 'None'}",
                f"- **Valid candidates:** {', '.join(oracle['valid_candidates']) or 'None'}",
                "",
                f"> {task['prompt']}",
                "",
            ]
        )

    output_path.write_text("\n".join(lines), encoding="utf-8")
    print(f"Ground truth: {output_path}")


if __name__ == "__main__":
    main()
